package Excercise;

public class Printer {
	// 6장 연습문제 16번
	
	static void println(int a){ //static은 class 바로아래라서 다른 클래스에서 객체생성없이 사용가능
		System.out.println(a);
	}
	
	static void println (boolean a) {
		System.out.println(a);
	}
	
	static void println (double a) {
		System.out.println(a);
	}
	
	static void println (String a) {
		System.out.println(a);
	}

}
